# You have two arrays: ["red", "green", "blue"] and ["yellow", "purple", "pink"]. Combine them into one array and print the result.

def printTwoarr(color_one, color_two)
  puts "#{color_one + color_two}"
end

color_one = ["red", "green", "blue"]
color_two = ["yellow", "purple", "pink"]

printTwoarr(color_one, color_two)